


// add 2 values

export function add(n1, n2) {
    return n1 + n2;

}

// multiply 2 values
export function multiply(n3, n4) {
    return n3 * n4;

}

// subtract 2 values
export function subtract(n5, n6) {
    return n5 - n6;
}


// divide 2 values
export function divide(n7, n8) {
    return n7 / n8;
}


// get the remainder of the division. 

export function remainder(n9, n10) {
    return n9 % n10;

}

// get the interger of the division

export function integer(n11, n12) {
    return Math.floor(n11 / n12);
}


// get pythagorean theorem

export function pythag(n13, n14) {
    return (n13 * n13) + (n14 * n14);
}